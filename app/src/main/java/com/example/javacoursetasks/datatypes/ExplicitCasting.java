package com.example.javacoursetasks.datatypes;

public class ExplicitCasting {

	public static void main(String[] args) {
		
		int a = 100;
		byte b = (byte) a;
		
		int c = 114;
		char ch = (char) c;
		
		int d = 1000;
		byte e = (byte) d;
		
		int f = 586;
		byte g = (byte) f;
		char ch2 = (char) g;
		
		System.out.println(b);
		System.out.println(ch);
		System.out.println(e);
		System.out.println(ch2);
		
		
		

	}

}

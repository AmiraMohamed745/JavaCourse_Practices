package com.example.javacoursetasks.datatypes;

public class ImplicitCasting {

	public static void main(String[] args) {

		byte a = 120;
		int b = a;
		System.out.println(b);
		
		long c = b;
		System.out.println(c);
		
		char d = 'F';
		int e = d;
		System.out.println(d);
		System.out.println(e);
	}

}
